/* Add setup ideal transmission line calls here                               */
static boolean fix_tr(
  UserInstDef *userInst)
{
  boolean status = TRUE;
  UserParamData *pData = userInst->pData;
  double a, b, len, k;
  double eta, vphase;
  double z0, td, loss;

  a = A_P;
  b = B_P;
  len = L_P;
  k = K_P;
  if (a <= 0.0 || b <= a || k < 1)
  {
    (void)sprintf(ErrMsg, "u2pb_fix(%s): invalid params: A=%g, B=%g, K=%g",
            userInst->tag, a, b, k);
    send_info_to_scn(ErrMsg);
    return FALSE;
  }
  eta = sqrt(MU0/EPS0/k);
  vphase = 1.0 / sqrt(MU0 * EPS0 * k);

  z0 = eta * log(b / a) / 2.0 / PI;
  td = len / vphase;
  loss = 1.0;   /*  lossless */

  status = add_tr_tline(userInst, 0, 1, GND, GND, z0, td, loss);
  return status;
}

