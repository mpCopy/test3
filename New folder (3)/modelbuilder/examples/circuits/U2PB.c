#define EPS0 8.854e-12
#define MU0  (4.0e-7*PI)
#define PI     3.141592654
#define ZO   50 /* reference impedance for S-pars */
#define sqr(x) ((x)*(x))

static char ErrMsg[101];

/* Add Y-parameter function calls here for LINEAR models only (the
   linear portion of a nonlinear model is added in the analyze_lin
   procedure).                                                                */
static boolean compute_y (
  UserInstDef *userInst,
  double omega,
  COMPLEX *yPar)
{
  boolean status = TRUE;
  double a, b, len, k;
  double eta, vphase, betal;
  double z, zzo, zoz;
  double arg1, arg2;
  double res11, res21;
  double denom;
  double ims11, ims21;
  COMPLEX S[4];
  UserParamData *pData = userInst->pData;

  a = A_P;
  b = B_P;
  len = L_P;
  k = K_P;
  if (a <= 0.0 || b <= a || k < 1)
  {
    (void)sprintf(ErrMsg, "U2PB compute_y(%s): invalid params: A=%g, B=%g, K=%g",
            userInst->tag, a, b, k);
    send_error_to_scn(ErrMsg);
    return FALSE;
  }
  eta = sqrt(MU0/EPS0/k);
  vphase = 1.0 / sqrt(MU0 * EPS0 * k);
  betal = omega * len / vphase;
  z = eta * log(b / a) / 2.0 / PI;
  zzo = z / ZO;
  zoz = ZO / z;
  arg1 = zzo - zoz;
  arg2 = zzo + zoz;
  denom = 4.0 * sqr(cos(betal)) + sqr(sin(betal)) * sqr(arg2);
  res11 = sqr(sin(betal)) * (sqr(zzo) - sqr(zoz)) / denom;
  ims11 = 2.0 * sin(betal) * cos(betal) * arg1 / denom;

  /* to avoid singularity, keep it small */
  if (res11 == 0.0)
    res11 = 1.0e-6;

  res21 = 4.0 * cos(betal) / denom;
  ims21 = -2.0 * sin(betal) * arg2 / denom;
  S[3].real = S[0].real = res11;
  S[3].imag = S[0].imag = ims11;
  S[2].real = S[1].real = res21;
  S[2].imag = S[1].imag = ims21;
  /* convert S[2x2] -> yPar[2x2] */
  return s_y_convert(S, yPar, 1, ZO, 2);
  return status;
}


