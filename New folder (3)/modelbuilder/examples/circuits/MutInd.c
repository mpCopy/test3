/* complex divide */
static COMPLEX complex_div(COMPLEX x, COMPLEX y)
{                    
        COMPLEX temp;
        double  denom;
        denom = (y.real * y.real) + (y.imag * y.imag);
        temp.real = ((x.real * y.real) + (x.imag * y.imag))/denom;
        temp.imag = ((x.imag * y.real) - (x.real * y.imag))/denom;
        return (temp);
}


/* complex negative */
static COMPLEX negative(COMPLEX x) 
{
        COMPLEX temp;
        temp.real = -x.real;
        temp.imag = -x.imag;
        return(temp);
}

