/* Add transient response here.                                               */
static boolean analyze_tr(
  UserInstDef *userInst,
  double *vPin)
{
  boolean status = TRUE;
  UserParamData *pData = userInst->pData;
  double L1, L2, R1, R2, K;
  int ind1,ind2;

  L1 = pData[0].value.dVal;
  L2 = pData[1].value.dVal;
  R1 = pData[2].value.dVal;
  R2 = pData[3].value.dVal;
  K =  pData[4].value.dVal;

  /* add_tr_lossy_inductor and add_tr_inductor return a integer 
     which is inductor index. The index should be larger than 0 */
  ind1 = add_tr_lossy_inductor(userInst, 0, 2, R1, L1);
  ind2 = add_tr_lossy_inductor(userInst, 1, 3, R2, L2);

  if ( ind1 && ind2 )
  {
      status = add_tr_mutual_inductor(userInst, ind1, ind2, K);
  }
  else
     status = FALSE;

  return status;
}


