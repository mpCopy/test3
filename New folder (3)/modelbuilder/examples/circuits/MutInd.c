/* Add Y-parameter function calls here for LINEAR models only (the
   linear portion of a nonlinear model is added in the analyze_lin
   procedure).                                                                */
static boolean compute_y (
  UserInstDef *userInst,
  double omega,
  COMPLEX *yPar)

{
    /**********************************************

          0        2 
          ----L1-----

          1        3
          ----L2-----

          ZL1 = R1 + j w * L1
          ZL2 = R2 + j w * L2
          Zm  = j w * M
          Zd = ( ZL1 * Zl2 - Zm * Zm)

                 [   ZL2     -Zm     -ZL2      Zm  ] [ ]   [ ]
          1/Zd * [  -Zm       ZL1     Zm      -ZL1 ] [V] = [I]
                 [  -ZL2      Zm      ZL2     -Zm  ] [ ]   [ ]
                 [   Zm      -ZL1    -Zm       ZL1 ] [ ]   [ ]

    **********************************************/

  double L1, L2, K, R1, R2, Rtiny, Lw1, Lw2, Lwm;
  UserParamData *pData = userInst->pData;
  COMPLEX zero, ZL1, ZL2, Zm, Zd;

  zero.real = 0.0;  zero.imag = 0.0;

  Rtiny = 1e-4;

  L1 = pData[0].value.dVal;
  L2 = pData[1].value.dVal;
  R1 = pData[2].value.dVal < Rtiny ? Rtiny : pData[2].value.dVal;
  R2 = pData[3].value.dVal < Rtiny ? Rtiny : pData[3].value.dVal;
  K =  pData[4].value.dVal;

  ZL1.real = R1; 
  ZL1.imag = Lw1 = omega * L1;
  ZL2.real = R2;
  ZL2.imag = Lw2 = omega * L2;

  if( L1 < 0 || L2 < 0)
  { 
    sprintf( ErrMsg, "Error: found invalid inductance in %s",
             userInst->tag);
    send_info_to_scn( ErrMsg );
    return FALSE;
  }

  Zm.real  = 0;
  Zm.imag  = Lwm = omega * K * sqrt( L1 * L2 );

  Zd.real = R1 * R2 - Lw1 * Lw2 + Lwm * Lwm;
  Zd.imag = R1 * Lw2 + R2 * Lw1;

  if(omega<=1.0)
  {
        yPar[0].real = yPar[10].real = 1/R1;
        yPar[0].imag = yPar[10].imag = 0.0;
        yPar[1] = yPar[11] = zero;
        yPar[2] = yPar[8]  = negative(yPar[0]);
        yPar[3] = yPar[9]  = zero;
        yPar[4] = yPar[14] = zero;
        yPar[5].real = yPar[15].real = 1/R2;
        yPar[5].imag = yPar[15].imag = 0.0;
        yPar[6] = yPar[12] = zero;
        yPar[7] = yPar[13] = negative(yPar[5]);
  } 
  else 
  {
        yPar[0] = yPar[10] = complex_div(ZL2,Zd); /*  ZL2/Zd */
        yPar[1] = yPar[11] = complex_div(negative(Zm),Zd);
        yPar[2] = yPar[8]  = negative(yPar[0]);   /* -ZL2/Zd */
        yPar[3] = yPar[9]  = negative(yPar[1]);   /* Zm / Zd */
        yPar[4] = yPar[14] = yPar[1];             /* -Zm/Zd  */
        yPar[5] = yPar[15] = complex_div(ZL1,Zd); /* ZL1/Zd  */
        yPar[6] = yPar[12] = yPar[3];             /* Zm / Zd */
        yPar[7] = yPar[13] = negative(yPar[5]);   /* -ZL1/Zd */
  }

  return TRUE;
}

